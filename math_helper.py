def add(a, b):
    """
    Return the sum of two numbers.

    Args:
        a: First number
        b: Second number

    Returns:
        The sum of a and b
    """
    return a + b

def subtract(a, b):
    """
    Return the difference of two numbers.

    Args:
        a: First number
        b: Second number

    Returns:
        The difference of a and b
    """
    return a - b
